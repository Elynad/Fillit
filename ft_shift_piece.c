#include "header.h"
#include "libft.h"

void	ft_shift_up_left(struct t_piece *a)
{
	while (a->l1 == 0)
	{
		a->l1 = a->l2;
		a->l2 = a->l3;
		a->l3 = a->l4;
		a->l4 = 0;
	}
	while (a->l1 < 32768 || a->l2 < 32768 || a->l3 < 32768 || a->l4 < 32768)
	{
		a->l1 *= 2;
		a->l2 *= 2;
		a->l3 *= 2;
		a->l4 *= 2;
	}
}
